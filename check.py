from database import db, app, User
from sqlalchemy import inspect

def check_database():
    with app.app_context():
        # 1. –ü—Ä–æ–≤–µ—Ä—è–µ–º —Å—É—â–µ—Å—Ç–≤–æ–≤–∞–Ω–∏–µ —Ç–∞–±–ª–∏—Ü
        inspector = inspect(db.engine)
